package com.maomao.ssm.pojo;

import java.io.Serializable;

/** 
* @author:wzy
* @descrption: 货源分类bean
* @version:
* @date:2018年3月6日
*/

public class SupplyCategory implements Serializable{
	private static final long serialVersionUID = -2355068293301492752L;
	private  Integer id;//分类id
	private String name;//分类名称
	private Integer categorySort;//前台排序
	private Integer stock;//分类商品总数
	private Integer brandcount;//品牌数量
	private Byte status;//分类状态 状态 0禁用 1启用 -1删除
	
	public Byte getStatus() {
		return status;
	}
	public void setStatus(Byte status) {
		this.status = status;
	}
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public Integer getCategorySort() {
		return categorySort;
	}
	public void setCategorySort(Integer categorySort) {
		this.categorySort = categorySort;
	}
	public Integer getStock() {
		return stock;
	}
	public void setStock(Integer stock) {
		this.stock = stock;
	}
	public Integer getBrandcount() {
		return brandcount;
	}
	public void setBrandcount(Integer brandcount) {
		this.brandcount = brandcount;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}
	
}































