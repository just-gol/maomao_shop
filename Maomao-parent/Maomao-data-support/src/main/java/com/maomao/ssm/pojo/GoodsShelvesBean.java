package com.maomao.ssm.pojo;

import java.io.Serializable;

/**
 * 商品下架分页查询参数
 * 
 * @author Administrator
 *
 */
public class GoodsShelvesBean implements Serializable {
	private Integer page; // 当前页
	private Integer rows; // 当前页条数
	private Integer startRows; //开始位置
	private String categoryName; // 商品分类名称
	private Byte goodsWarehouseType; // 存储方式
	private Byte goodsType; // 商品性质
	private String goodsName; // 商品名称
	private Integer startStock; //存储条件 开始
	private Integer endStock;//存储条件 结束
	public GoodsShelvesBean() {
	}
	public GoodsShelvesBean(Integer page, Integer rows, Integer startRows, String categoryName, Byte goodsWarehouseType,
			Byte goodsType, String goodsName, Integer startStock, Integer endStock) {
		super();
		this.page = page;
		this.rows = rows;
		this.startRows = startRows;
		this.categoryName = categoryName;
		this.goodsWarehouseType = goodsWarehouseType;
		this.goodsType = goodsType;
		this.goodsName = goodsName;
		this.startStock = startStock;
		this.endStock = endStock;
	}
	public Integer getPage() {
		return page;
	}
	public void setPage(Integer page) {
		this.page = page;
	}
	public Integer getRows() {
		return rows;
	}
	public void setRows(Integer rows) {
		this.rows = rows;
	}
	public Integer getStartRows() {
		return startRows;
	}
	public void setStartRows(Integer startRows) {
		this.startRows = startRows;
	}
	public String getCategoryName() {
		return categoryName;
	}
	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}
	public Byte getGoodsWarehouseType() {
		return goodsWarehouseType;
	}
	public void setGoodsWarehouseType(Byte goodsWarehouseType) {
		this.goodsWarehouseType = goodsWarehouseType;
	}
	public Byte getGoodsType() {
		return goodsType;
	}
	public void setGoodsType(Byte goodsType) {
		this.goodsType = goodsType;
	}
	public String getGoodsName() {
		return goodsName;
	}
	public void setGoodsName(String goodsName) {
		this.goodsName = goodsName;
	}
	public Integer getStartStock() {
		return startStock;
	}
	public void setStartStock(Integer startStock) {
		this.startStock = startStock;
	}
	public Integer getEndStock() {
		return endStock;
	}
	public void setEndStock(Integer endStock) {
		this.endStock = endStock;
	}
}
