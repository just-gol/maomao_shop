package com.maomao.ssm.bean;

import java.io.Serializable;
import java.util.Date;

public class UserApplyCustom implements Serializable {
	private static final long serialVersionUID = 1L;
	private Integer id;
	private String account;
	private String name;
	private Byte creditType;
	private Long leaveMoney;
	private Long totalMoney;
	private Long useMoney;
	private Date time;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Byte getCreditType() {
		return creditType;
	}

	public void setCreditType(Byte creditType) {
		this.creditType = creditType;
	}

	public Long getLeaveMoney() {
		return leaveMoney;
	}

	public void setLeaveMoney(Long leaveMoney) {
		this.leaveMoney = leaveMoney;
	}

	public Long getTotalMoney() {
		return totalMoney;
	}

	public void setTotalMoney(Long totalMoney) {
		this.totalMoney = totalMoney;
	}

	public Long getUseMoney() {
		return useMoney;
	}

	public void setUseMoney(Long useMoney) {
		this.useMoney = useMoney;
	}

	public Date getTime() {
		return time;
	}

	public void setTime(Date time) {
		this.time = time;
	}

}
