public class MyShop implements Serializable{
	private static final long serialVersionUID = 6942389190010061140L;
	private long money;//受益金额
    private ShopShare shopShare;
    private ShopUnShare shopUnShare;
    private Account account;
    
	public MyShop(long money, ShopShare shopShare, ShopUnShare shopUnShare, Account account) {
		this.money = money;
		this.shopShare = shopShare;
		this.shopUnShare = shopUnShare;
		this.account = account;
	}
	public MyShop() {
	}
	public long getMoney() {
		return money;
	}
	public void setMoney(long money) {
		this.money = money;
	}
	public ShopShare getShopShare() {
		return shopShare;
	}
	public void setShopShare(ShopShare shopShare) {
		this.shopShare = shopShare;
	}
	public ShopUnShare getShopUnShare() {
		return shopUnShare;
	}
	public void setShopUnShare(ShopUnShare shopUnShare) {
		this.shopUnShare = shopUnShare;
	}
	public Account getAccount() {
		return account;
	}
	public void setAccount(Account account) {
		this.account = account;
	}
	
    
}
