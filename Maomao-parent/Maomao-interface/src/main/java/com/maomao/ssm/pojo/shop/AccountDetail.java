package com.maomao.ssm.pojo.shop;

import java.io.Serializable;
import java.util.List;

/** 
* @author:wzy
* @descrption:账户详情
* @version:
* @date:2018年1月28日
*/

public class AccountDetail implements Serializable{
	private Long totalMoney;//总收益=累计提现+可提现
	private Long accumulativeMoney;//累计提现
	private Long money;//可提现金额
	private List<AccountLogs> accountLogs;//账户流水详情
	public Long getTotalMoney() {
		return totalMoney;
	}
	public void setTotalMoney(Long totalMoney) {
		this.totalMoney = totalMoney;
	}
	public Long getAccumulativeMoney() {
		return accumulativeMoney;
	}
	public void setAccumulativeMoney(Long accumulativeMoney) {
		this.accumulativeMoney = accumulativeMoney;
	}
	public Long getMoney() {
		return money;
	}
	public void setMoney(Long money) {
		this.money = money;
	}
	public List<AccountLogs> getAccountLogs() {
		return accountLogs;
	}
	public void setAccountLogs(List<AccountLogs> accountLogs) {
		this.accountLogs = accountLogs;
	}
	
}




































