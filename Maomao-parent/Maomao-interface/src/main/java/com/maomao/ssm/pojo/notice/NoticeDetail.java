package com.maomao.ssm.pojo.notice;

import java.io.Serializable;
import java.util.Date;

import com.maomao.ssm.bean.NoticeRecordWithBLOBs;

/** 
* @author:wzy
* @descrption:
* @version:
* @date:2018年1月26日
*/

public class NoticeDetail  implements Serializable{
	private Date createTime;
	private String content;
	public NoticeDetail() {
		
	}
	public NoticeDetail(NoticeRecordWithBLOBs noticeRecordWithBLOBs) {
		this.createTime=noticeRecordWithBLOBs.getCreateTime();
		this.content= noticeRecordWithBLOBs.getContent();
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	
}


























