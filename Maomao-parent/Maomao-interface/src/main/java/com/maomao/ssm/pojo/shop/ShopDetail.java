package com.maomao.ssm.pojo.shop;

import java.io.Serializable;

/** 
* @author:wzy
* @descrption:店铺详情
* @version:
* @date:2018年1月28日
*/

public class ShopDetail implements Serializable{
	private Long money;//当前收益金额
	private Long total;//认筹总金额
	private Long surplus;//剩余认筹金额
	private Integer goodsNum;//已上架商品
	private Integer subscriptionNum;//认筹中商品
	
	public Long getMoney() {
		return money;
	}
	public void setMoney(Long money) {
		this.money = money;
	}
	public Long getTotal() {
		return total;
	}
	public void setTotal(Long total) {
		this.total = total;
	}
	public Long getSurplus() {
		return surplus;
	}
	public void setSurplus(Long surplus) {
		this.surplus = surplus;
	}
	public Integer getGoodsNum() {
		return goodsNum;
	}
	public void setGoodsNum(Integer goodsNum) {
		this.goodsNum = goodsNum;
	}
	public Integer getSubscriptionNum() {
		return subscriptionNum;
	}
	public void setSubscriptionNum(Integer subscriptionNum) {
		this.subscriptionNum = subscriptionNum;
	}
	
}







































